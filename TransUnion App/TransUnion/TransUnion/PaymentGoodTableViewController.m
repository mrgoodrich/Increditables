#pragma mark - Table view data source

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView {
    // Return the number of sections.
    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section {
    return [self.paymentHistory count];
}


- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:@"paymentHistory" forIndexPath:indexPath];
    UILabel *label = (UILabel *)[cell viewWithTag:101];
    label.text = self.paymentHistory[indexPath.row];
    UIButton *backButton = (UIButton *)[cell viewWithTag:102];
    backButton.hidden = NO;
    if (!indexPath.row == 0){
        [backButton setHidden:YES];
    }
    [cell setSelectionStyle:UITableViewCellSelectionStyleNone];
    return cell;
}


- (IBAction)backButton:(id)sender {
    [self.navigationController popViewControllerAnimated:YES];
}


